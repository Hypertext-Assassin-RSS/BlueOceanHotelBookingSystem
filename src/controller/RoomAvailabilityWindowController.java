package controller;

import javafx.event.ActionEvent;
import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.stage.Stage;

import java.io.IOException;
import java.net.URL;

public class RoomAvailabilityWindowController {
    public void openWindow4(ActionEvent actionEvent) throws IOException {
        URL resource = getClass().getResource("../view/CustomerWindow.fxml");
        Parent load = FXMLLoader.load(resource);
        Scene scene = new Scene(load);
        Stage stage = new Stage();
        stage.setScene(scene);
        stage.show();
    }

    public void openWindow2(ActionEvent actionEvent) throws IOException {
        URL resource = getClass().getResource("../view/RoomAndPackageWindow.fxml");
        Parent load = FXMLLoader.load(resource);
        Scene scene = new Scene(load);
        Stage stage = new Stage();
        stage.setScene(scene);
        stage.show();
    }

}
